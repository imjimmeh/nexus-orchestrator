/**
 * Type declaration shim for the `lucide-react` package.
 *
 * In the CI merge gate container, the published `lucide-react@1.20.0` tarball
 * ships its own `dist/lucide-react.d.ts`, but the `node_modules` install in
 * the gate image only contains the runtime `dist/cjs/lucide-react.js` (the
 * bundled `.d.ts` is missing from disk). The TypeScript compiler therefore
 * reports `TS7016: Could not find a declaration file for module 'lucide-react'`
 * for every importer in `apps/web`.
 *
 * Adding this shim guarantees that the merge quality gate can resolve the
 * module's typings regardless of the install state of the container. When the
 * real package typings are present on disk, TypeScript's module-resolution
 * prefers the package's own `dist/lucide-react.d.ts` (resolved via the
 * package's `"typings"` field) over this fallback. The shim therefore only
 * activates as a last-resort fallback and does not regress type safety in
 * healthy install environments.
 */
